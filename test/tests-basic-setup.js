define(['chai', 'jquery', 'answers'], function(chai, $, answers) {

	var assert = chai.assert;

    describe('Basic setup', function() {

        it('should work', function() {

	        assert.typeOf(answers, 'object', 'answers should be an object');

	        assert.typeOf(answers.Important, 'string');
	        assert.equal(answers.Important, 'Do not edit this file!');

	        assert.typeOf(answers.because, 'function');
	        assert.equal(answers.because(), 'this is just an example!');
        });
    });
});
